package anajavatica.junit;

public class MathOperations {

	public int sum(int val1, int val2) {
		return val1 + val2;
	}

	public int diff(int val1, int val2) {
		return val1 - val2;
	}

	public int mul(int val1, int val2) {
		return val1 * val2;
	}

	public int div(int val1, int val2) {
		return val1 / val2;
	}

}
